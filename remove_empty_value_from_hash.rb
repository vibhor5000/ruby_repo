h = {2013 => { "IND" => 2, "US" => "" }, 2014 => {"IND" => "", "US" => ""}, 2015 => {"IND" => nil, "US" => 6}}

def remove_empty_values(h)
    h.each do |k, v|
        v.delete_if do |k, v| 
            v == "" || v == nil
        end 
    end 
end

puts remove_empty_values(h)

